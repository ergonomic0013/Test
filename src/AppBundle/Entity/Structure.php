<?php

namespace AppBundle\Entity;

/**
 * Structure
 */
class Structure
{
    /**
     * @var int
     */
    private $id;

    /**
     * @var string
     */
    private $name;

    /**
     * @var string
     */
    private $position;

    /**
     * @var \DateTime
     */
    private $dateEnter;

    /**
     * @var int
     */
    private $salary;

    /**
     * @var int
     */
    private $parentId;

    /**
     * @var int
     */
    private $depth;


    /**
     * Get id
     *
     * @return int
     */
    public function getId()
    {
        return $this->id;
    }

    /**
     * Set name
     *
     * @param string $name
     *
     * @return Structure
     */
    public function setName($name)
    {
        $this->name = $name;

        return $this;
    }

    /**
     * Get name
     *
     * @return string
     */
    public function getName()
    {
        return $this->name;
    }

    /**
     * Set position
     *
     * @param string $position
     *
     * @return Structure
     */
    public function setPosition($position)
    {
        $this->position = $position;

        return $this;
    }

    /**
     * Get position
     *
     * @return string
     */
    public function getPosition()
    {
        return $this->position;
    }

    /**
     * Set dateEnter
     *
     * @param \DateTime $dateEnter
     *
     * @return Structure
     */
    public function setDateEnter($dateEnter)
    {
        $this->dateEnter = $dateEnter;

        return $this;
    }

    /**
     * Get dateEnter
     *
     * @return \DateTime
     */
    public function getDateEnter()
    {
        return $this->dateEnter;
    }

    /**
     * Set salary
     *
     * @param integer $salary
     *
     * @return Structure
     */
    public function setSalary($salary)
    {
        $this->salary = $salary;

        return $this;
    }

    /**
     * Get salary
     *
     * @return int
     */
    public function getSalary()
    {
        return $this->salary;
    }

    /**
     * Set parentId
     *
     * @param integer $parentId
     *
     * @return Structure
     */
    public function setParentId($parentId)
    {
        $this->parentId = $parentId;

        return $this;
    }

    /**
     * Get parentId
     *
     * @return int
     */
    public function getParentId()
    {
        return $this->parentId;
    }

    /**
     * Set depth
     *
     * @param integer $depth
     *
     * @return Structure
     */
    public function setDepth($depth)
    {
        $this->depth = $depth;

        return $this;
    }

    /**
     * Get depth
     *
     * @return int
     */
    public function getDepth()
    {
        return $this->depth;
    }
}
